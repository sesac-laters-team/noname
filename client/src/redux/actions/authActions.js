const SIGNIN = "authActions/SIGNIN";
const SIGNUP = "authActions/SIGNUP";
const SIGNUP_FAIL = "authActions/SIGNUP_FAIL";

const login = (data) => {
    return {
        type: SIGNIN,
        payload: data,
    };
};
const register = (data) => {
    return {
        type: SIGNUP,
        payload: data,
    };
};

const registerFail = (error) => {
    return {
        type: SIGNUP_FAIL,
        payload: error,
    };
};

export { SIGNIN, SIGNUP, SIGNUP_FAIL, login, register, registerFail };
